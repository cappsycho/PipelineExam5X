CREATE OR REPLACE TABLE RECOVERY_PER_MONTH (
    TOTAL_RECOVERED NUMBER(38,0),
    LOCATION VARCHAR(256),
    "MONTH" VARCHAR(256),
    "YEAR" VARCHAR(4)
);

INSERT INTO RECOVERY_PER_MONTH (TOTAL_RECOVERED, LOCATION,"MONTH","YEAR")
SELECT SUM(TOTAL_RECOVERED), LOCATION,"MONTH","YEAR" FROM (SELECT TOTAL_RECOVERED, LOCATION,
MONTHNAME(to_date("DATE", 'MM/DD/YYYY')) as "MONTH", YEAR(to_date("DATE", 'MM/DD/YYYY')) as "YEAR" FROM "FIVETRAN_INTERVIEW_DB"."GOOGLE_SHEETS_YR"."FIVETRAN_INTERVIEW_DB_YR" WHERE "DATE" != 'Date')
GROUP BY LOCATION, "MONTH","YEAR";