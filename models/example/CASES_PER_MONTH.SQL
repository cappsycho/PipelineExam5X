CREATE OR REPLACE TABLE CASES_PER_MONTH (
    TOTAL_CASES NUMBER(38,0),
    LOCATION VARCHAR(256),
    "MONTH" VARCHAR(256),
    "YEAR" VARCHAR(4)
);

INSERT INTO CASES_PER_MONTH (TOTAL_CASES, LOCATION,"MONTH","YEAR")
SELECT SUM(TOTAL_CASES), LOCATION,"MONTH","YEAR" FROM (SELECT TOTAL_CASES, LOCATION,
MONTHNAME(to_date("DATE", 'MM/DD/YYYY')) as "MONTH", YEAR(to_date("DATE", 'MM/DD/YYYY')) as "YEAR" FROM "FIVETRAN_INTERVIEW_DB"."GOOGLE_SHEETS_YR"."FIVETRAN_INTERVIEW_DB_YR" WHERE "DATE" != 'Date')
GROUP BY LOCATION, "MONTH","YEAR";